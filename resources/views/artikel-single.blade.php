                        <div> Tanggal 12 Juli menjadi momentum Bangsa 
Indonesia dalam memperingati hari lahirnya Koperasi. Koperasi bagaikan 
jantungnya ekonomi bangsa Indonesia. Setiap pergerakan turun naiknya 
perekonomian seakan terus berpacu bersama jantungnya koperasi. Koperasi 
telah menjadi salah satu penggerak terbesar roda perekonomian bangsa.<br> <br>
 Sejarah singkat gerakan koperasi bermula pada abad ke-20 yang pada 
umumnya merupakan hasil dari usaha yang tidak spontan dan tidak 
dilakukan oleh orang-orang yang sangat kaya. Koperasi tumbuh dari 
kalangan rakyat, ketika penderitaan dalam lapangan ekonomi dan sosial 
yang ditimbulkan oleh sistem kapitalisme semakin memuncak. Beberapa 
orang yang penghidupannya sederhana dengan kemampuan ekonomi terbatas, 
terdorong oleh penderitaan dan beban ekonomi yang sama, secara spontan 
mempersatukan diri untuk menolong dirinya sendiri dan manusia sesamanya.<br> <br>
 Di Indonesia, ide-ide perkoperasian diperkenalkan pertama kali oleh 
oleh R. Aria Wiriatmadja patih di Purwokerto. Koperasi adalah sebuah 
organisasi ekonomi yang dimiliki dan dioperasikan oleh orang-seorang 
demi kepentingan bersama.Koperasi melandaskan kegiatan berdasarkan 
prinsip gerakan ekonomi rakyat yang berdasarkan asas kekeluargaan.<br> <br>
 Berdasarkan Data Kementerian Koperasi dan UKM serta BPS dari tahun ke 
tahun, tepatnya tahun 2014, kontribusi koperasi terhadap PDB Nasional 
sejumlah 1,71 persen, selanjutnya tahun 2016 sudah meningkat menjadi 
3,99 persen dan tahun 2017, kontribusi koperasi terhadap PDB nasional 
sudah meningkat lagi sebesar 4,48 persen.<br> Seperti apa pergerakan 
Koperasi di Provinsi Kepulauan Bangka Belitung? Berdasarkan data 
Departemen Kementerian Koperasi, Pertumbuhan Koperasi di Provinsi 
Kepulauan Bangka Belitung, pada tahun 2017 jumlah koperasi berjumlah 981
 koperasi, dimana koperasi yang aktif sebanyak 744 dan 237 koperasi 
tidak aktif. Sedangkan pada tahun 2018 jumlah koperasi di Provinsi 
Kepulauan Bangka Belitung sedikit meningkat berjumlah 996 dengan jumlah 
koperasi aktif sebanyak 714 koperasi, sedangkan koperasi yang tidak 
aktif 282 koperasi.<br> <br> Naik turunnya jumlah koperasi yang ada disebabkan adanya koperasi aktif menjadi tidak aktif sehingga di lakukan pembubaran.<br>
 Begitu besarnya peran dan kontribusi koperasi bagi keberlangsungan roda
 perekonomian saat ini, terlepas dari keterkaitan akan roda 
perekonomian. Tanpa bisa dipungkiri, koperasi secara perlahan terus 
memberikan kontribusi positif bagi roda perekonomian bangsa, ekonomi 
rakyat seperti dunia usaha yang menjadi anggota koperasi pun tentunya 
ikut merasakan dampak positif dengan hadirnya koperasi.<br> <br> 
Pentingnya peran Koperasi bagi keberlangsungan Perekonomian sosial 
sesuai dengan amanat dalam UUD 1945 Pasal 33, menyatakan bahwa 
perekonomian disusun sebagai usaha bersama berdasar atas azas 
kekeluargaan. Di Indonesia sendiri telah dibuat UU no. 25 tahun 1992 
tentang Perkoperasian. Prinsip koperasi menurut UU no. 25 tahun 1992 
adalah: 1) Keanggotaan bersifat sukarela dan terbuka; 2) pengelolaan 
dilakukan secara demokrasi; 3) pembagian SHU dilakukan secara adil 
sesuai dengan jasa usaha masing-masing anggota; 4) Pemberian balas jasa 
yang terbatas terhadap modal; 5) Kemandirian; 6) Pendidikan 
perkoperasian; 7) Kerjasama antar koperasi.<br> <br> Menjamurnya 
pergerakan usaha mikro kecil dan menengah (UMKM) menjadi peluang 
tumbuhnya ekonomi kreatif masyarakat saat ini. Kemandirian para pelaku 
usaha secara perlahan membuka gerbang terbukanya lapangan usaha baik 
dibidang produksi, pemasaran maupun jasa.<br> <br> Terbangunnya ekonomi 
kreatif di berbagai kalangan masyarakat yang bergerak di tingkat usaha, 
baik usaha skala kecil, menengah maupun skala besar. Saat ini, dimana 
Era serba digitalisasi saat ini, menjadi tantangan yang besar bagi dunia
 koperasi. Efesien dan efektif serta cepat dan tanggap menjadi tuntutan 
yang wajib bagi koperasi, dibutuhkannya reformasi koperasi dimulai dari 
koperasi skala kecil hingga besar dalam menghadapi era globalisasi.<br> <br>
 Begitu besarnya peran koperasi terhadap keberlangsungan usaha menengah 
kecil dan mikro (UMKM) di Provinsi Kepulauan Bangka Belitung, terbukti, 
hingga tahun 2018 pertumbuhan UMKM di Provinsi Bangka Belitung semakin 
meningkat, jumlah UMKM di Bangka Belitung sebanyak 180.509, yang terdiri
 dari Kota Pangkalpinang 17.308, Kabupaten Bangka 3.617, Kabupaten 
Belitung 20.491, Kabupaten Bangka Barat 23.813, Kabupaten Bangka Selatan
 6.593 dan Kabupaten Belitung Timur 18.891.<br> <br> Pelayanan dan 
kinerja Koperasi yang profesional dan berkualitas menjadi tolak ukur 
keberhasilan koperasi tersebut. Pola pikir dari setiap anggota koperasi 
dalam membangkitkan eksistensi dan marwah koperasi harus sesuai dengan 
misi dan visi yang ada, sehingga terwujudnya kesejahteraan bersama dalam
 mewujudkan ekonomi nasional. </div>
